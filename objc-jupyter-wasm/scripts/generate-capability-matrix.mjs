#!/usr/bin/env node
import { execFile } from "node:child_process";
import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { promisify } from "node:util";

const execFileAsync = promisify(execFile);
const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const repositoryRoot = path.resolve(projectRoot, "..");
const args = process.argv.slice(2);

function argValue(name) {
  const index = args.indexOf(name);
  return index >= 0 ? args[index + 1] : null;
}

const kernel = argValue("--kernel");
const output = argValue("--output");
const smokeSite = argValue("--smoke-site");

if (!kernel || !output) {
  throw new Error("Usage: generate-capability-matrix.mjs --kernel PATH --output PATH [--smoke-site PATH]");
}

async function run(command, commandArgs, options = {}) {
  const { stdout, stderr } = await execFileAsync(command, commandArgs, {
    cwd: repositoryRoot,
    env: process.env,
    maxBuffer: 8 * 1024 * 1024,
    ...options,
  });
  return `${stdout}${stderr}`;
}

function requireMatch(outputText, pattern, label) {
  const match = outputText.match(pattern);
  if (!match) throw new Error(`Could not read ${label} from test output:\n${outputText}`);
  return match;
}

const smoke = await run("node", ["objc-jupyter-wasm/tests/kernel-smoke.mjs", kernel]);
requireMatch(smoke, /kernel smoke passed/, "kernel smoke result");

const runtimeV2 = await run("node", ["objc-jupyter-wasm/tests/test-runtime-v2.mjs", kernel]);
requireMatch(runtimeV2, /ALL TESTS PASSED/, "runtime v2 result");

const gaps = await run("node", ["objc-jupyter-wasm/tests/test-runtime-gap-probes.mjs", kernel]);
const gapMatch = requireMatch(gaps, /Total:\s+(\d+)\/(\d+) passed/, "runtime gap result");

const compatibility = await run("node", [
  "objc-jupyter-wasm/tests/test-garazyk-compat.mjs",
  "--kernel",
  kernel,
]);
const compatibilityMatch = requireMatch(
  compatibility,
  /Garazyk compatibility corpus passed\s+\((\d+)\/(\d+)\)/,
  "compatibility result",
);

const notebooks = await run("node", [
  "objc-jupyter-wasm/tests/run-notebooks.mjs",
  "--kernel",
  kernel,
  "--dir",
  "objc-jupyter-wasm/demo",
]);
const notebookMatch = requireMatch(
  notebooks,
  /All\s+(\d+) notebooks passed\s+\((\d+)\/(\d+) cells,\s+(\d+) skipped\)/,
  "notebook result",
);

let browserStatus = "Not run";
let browserEvidence = "Requires `PLAYWRIGHT_CHROMIUM_EXECUTABLE_PATH` or a Playwright-managed Chromium.";
if (smokeSite) {
  const browser = await run("node", ["objc-jupyter-wasm/tests/browser-smoke.mjs", smokeSite]);
  requireMatch(browser, /browser smoke passed/, "browser smoke result");
  browserStatus = "Passed";
  browserEvidence = "Worker, content-hashed WASM asset, kernel-info request, and execute request completed in Chromium.";
}

const commit = (await run("git", ["rev-parse", "HEAD"])).trim();
const worktreeState = await run("git", ["status", "--porcelain"]);
const sourceRevision = worktreeState.trim() ? `${commit} (dirty worktree)` : commit;
const generatedAt = new Date().toISOString();
const markdown = `# Objective-C Jupyter WASM capability matrix

> Generated by \`scripts/run-capability-baseline.sh\` on ${generatedAt} from source revision
> \`${sourceRevision}\`.
> Regenerate this file; do not edit its result tables by hand.

## Verified baseline

| Surface | Result | Evidence |
| --- | --- | --- |
| Reproducible kernel artifact | Passed | The runner builds \`kernel-wasm\` twice and requires the same Nix store path. |
| Kernel C/JSON ABI smoke | Passed | \`kernel-smoke.mjs\` completed against \`kernel.wasm\`. |
| Runtime v2 suite | Passed | \`test-runtime-v2.mjs\` reported all tests passed. |
| Objective-C interpreter gap probes | ${gapMatch[1]}/${gapMatch[2]} passed | \`test-runtime-gap-probes.mjs\`. |
| Demo notebooks | ${notebookMatch[1]} notebooks; ${notebookMatch[2]}/${notebookMatch[3]} cells | ${notebookMatch[4]} cells are explicit skips, not passing cells. |
| Garazyk compatibility corpus | ${compatibilityMatch[1]}/${compatibilityMatch[2]} passed | Interpreter snippets, host-bridge self-tests, and expected unsupported-API diagnostics. |
| Nix smoke-site asset check | Passed | Nix verifies the static site, content-hashed WASM asset, runtime manifest, and current demo notebook fixture. |
| Browser worker smoke | ${browserStatus} | ${browserEvidence} |

## Support boundary

| Classification | What this baseline proves | What it does not claim |
| --- | --- | --- |
| Supported interpreter slice | The tested Objective-C interpreter features, JSON kernel ABI, stream output, and notebook examples execute in \`kernel.wasm\`. | It is not a general Objective-C compiler or a GNUstep Foundation implementation. |
| Supported browser smoke slice | The static smoke site loads the content-addressed kernel through a worker and completes one kernel-info and execute round trip. | It is not the packaged JupyterLab extension or a multi-user production deployment. |
| Guarded production APIs | The compatibility corpus recognizes unavailable production dependencies and emits expected diagnostics instead of attempting unsafe partial execution. | A diagnostic is not an implementation of SQLite, GCD, filesystem, Keychain/Security, or network service APIs. |
| Deferred compile plane | The repository documents Emscripten side modules as the path for compiled Objective-C cells. | No compile service, artifact cache, dynamic side-module loader, interactive compiled-cell evaluation, cross-cell variable inspection, code completion, object introspection, or full Jupyter exception presentation is verified here. |

## Reproduction

From the repository root, run:

\`\`\`bash
PLAYWRIGHT_CHROMIUM_EXECUTABLE_PATH=/path/to/chromium \\
  bash objc-jupyter-wasm/scripts/run-capability-baseline.sh
\`\`\`

The browser path is optional. Without it, the matrix records the browser row as not run while all
kernel, runtime, notebook, compatibility, and Nix asset checks still execute. The current browser
evidence used Chromium at \`/Applications/Chromium.app/Contents/MacOS/Chromium\` on macOS.
`;

await mkdir(path.dirname(output), { recursive: true });
await writeFile(output, markdown);
console.log(`Wrote capability matrix to ${output}`);
